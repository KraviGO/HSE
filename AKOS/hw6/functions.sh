# Функция приветствия
say_hello() {
    echo "Привет, $1!"
}

# Функция сложения
add_numbers() {
    result=$(( $1 + $2 ))
    echo "Сумма: $1 + $2 = $result"
}

# Функция которая вызывает другую функцию
calculate() {
    echo "Начинаем вычисления..."
    add_numbers $1 $2  # Вызов другой функции
    echo "Вычисления завершены"
}

# Главная функция
main() {
    say_hello "Пользователь"
    calculate 5 3
    calculate 10 20
}

# Запуск
main